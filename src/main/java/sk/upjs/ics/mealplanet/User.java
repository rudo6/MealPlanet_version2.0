package sk.upjs.ics.mealplanet;

public class User {
    
    private Long id;
    
    private String uzivatelskeMeno;
    
    private String heslo;
    
    private String meno;
    
    private String priezvisko;
    
    private String adresa;
    
    private String telefonneCislo;
    
    private String email;

    /**
     * @return the id
     */
    public Long getId() {
        return id;
    }

    /**
     * @param id the id to set
     */
    public void setId(Long id) {
        this.id = id;
    }

    /**
     * @return the uzivatelskeMeno
     */
    public String getUzivatelskeMeno() {
        return uzivatelskeMeno;
    }

    /**
     * @param uzivatelskeMeno the uzivatelskeMeno to set
     */
    public void setUzivatelskeMeno(String uzivatelskeMeno) {
        this.uzivatelskeMeno = uzivatelskeMeno;
    }

    /**
     * @return the heslo
     */
    public String getHeslo() {
        return heslo;
    }

    /**
     * @param heslo the heslo to set
     */
    public void setHeslo(String heslo) {
        this.heslo = heslo;
    }

    /**
     * @return the meno
     */
    public String getMeno() {
        return meno;
    }

    /**
     * @param meno the meno to set
     */
    public void setMeno(String meno) {
        this.meno = meno;
    }

    /**
     * @return the priezvisko
     */
    public String getPriezvisko() {
        return priezvisko;
    }

    /**
     * @param priezvisko the priezvisko to set
     */
    public void setPriezvisko(String priezvisko) {
        this.priezvisko = priezvisko;
    }

    /**
     * @return the adresa
     */
    public String getAdresa() {
        return adresa;
    }

    /**
     * @param adresa the adresa to set
     */
    public void setAdresa(String adresa) {
        this.adresa = adresa;
    }

    /**
     * @return the telefonneCislo
     */
    public String getTelefonneCislo() {
        return telefonneCislo;
    }

    /**
     * @param telefonneCislo the telefonneCislo to set
     */
    public void setTelefonneCislo(String telefonneCislo) {
        this.telefonneCislo = telefonneCislo;
    }

    /**
     * @return the email
     */
    public String getEmail() {
        return email;
    }

    /**
     * @param email the email to set
     */
    public void setEmail(String email) {
        this.email = email;
    }
    
    
    
}
